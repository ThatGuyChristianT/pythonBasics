##Data Types
#Numbers (Arithmetic)

#Addition
equation_add = 1+1
print('Result of 1 + 1 is', equation_add)#2

#Multiplication
equation_multiplication = 1*2
print('Result of 1 * 2 is', equation_multiplication)#2

#Division
equation_division = 4/2
print('Result of 4 / 2 is', equation_division)#2.0

#Exponentiation
equation_exponentiation = 2**2
print('Result of 2**2 is', equation_exponentiation)#4

#Modularization

equation_modularization_with_remainder = 1%2
print('Result of 1 % 2 is', equation_modularization_with_remainder, '(Has remainder)')#1

equation_modularization_without_remainder = 1%1
print('Result of 1 % 1 is', equation_modularization_without_remainder, '(Does not have remainder)')#0

